    private String ParaSexo() {
        String sexo = null;
        if (JRB_PMasculino.isSelected()) {
            sexo = "Masculino";
        } else if (JRB_PFemenino.isSelected()) {
            sexo = "Femenino";
        }
        return sexo;
    }

